#include <iostream>

using namespace std;

int main()
{
    int a, b;
    cout << "Enter two numbers:";
    cin >> a;
    cin >> b;

    cout << "a + b: " << a + b << endl;
    cout << "a - b: " << a - b << endl;
    cout << "a * b: " << a * b << endl;
    cout << "a / b: " << a / b << endl;
    cout << "a % b: " << a % b << endl;

    return 0;
}
